#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new;
	int		length;
	int		i;

	if (s == NULL)
		return (NULL);
	i = 0;
	length = ft_strlen((char *)s);
	new = ft_strnew(length);
	if (new == NULL)
		return (NULL);
	while (s[i])
	{
		new[i] = (*f)(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
